package com.api.reactive_nutritionapi.domain.constants.entity;

public class Pair {

    private String key;
    private String value;

    public String getKey() {
        return key;
    }

    public Pair setKey(String key) {
        this.key = key;
        return this;
    }
    public String getValue() {
        return value;
    }

    public Pair setValue(String value) {
        this.value = value;
        return this;
    }

}
